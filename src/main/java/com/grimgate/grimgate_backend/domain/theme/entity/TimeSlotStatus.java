package com.grimgate.grimgate_backend.domain.theme.entity;

/**
 * 타임슬롯의 상태를 나타내는 Enum 클래스입니다.
 * 
 * [상태값을 응답에 포함하는 이유]
 * - 사용자에게 예약 가능 여부를 실시간으로 제공하여 중복 예약을 사전에 차단하기 위해 필요합니다.
 * - 클라이언트는 이 상태값을 바탕으로 화면 상에 예약 가능(SLOT_AVAILABLE), 선점중(SLOT_HELD),
 *   예약 완료(SLOT_FULL)에 따라 버튼의 활성화 여부나 색상을 변경하는 등 동적인 UI 렌더링을 수행할 수 있습니다.
 * - 다수의 사용자가 동시에 동일한 테마의 특정 시간대를 조회할 때, 동시성 관점에서 현재 예약 진행 중인 상태(HELD)를
 *   노출시킴으로써 불필요한 예약 요청 시도를 방지하고 전반적인 사용자 경험(UX)을 개선합니다.
 */
public enum TimeSlotStatus {
    /**
     * 예약 가능한 상태입니다.
     */
    SLOT_AVAILABLE,

    /**
     * 다른 사용자에 의해 임시 선점된 상태입니다. (예: 결제 대기 중)
     */
    SLOT_HELD,

    /**
     * 예약이 완료되어 더 이상 예약할 수 없는 상태입니다.
     */
    SLOT_FULL
}
